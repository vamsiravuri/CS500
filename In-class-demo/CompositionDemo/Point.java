
public class Point {
	
	private double cx;
	private double cy;
	public Point(double x, double y)
	{
		cx = x;
		cy = y;
	}
	
	public void setCx(double x)
	{
		cx = x;
	}
	
	public void setCy(double y)
	{
		cy = y;
	}
	
	public double getCx()
	{
		return cx;
	}
	
	public double getCy()
	{
		return cy;
	}
		
	

}
